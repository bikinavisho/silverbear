          Add potatoes and enough water to cover the vegetables.
          Bring to a boil. Maintain a low boil until potatoes are tender,
          but not overcooked. Add peas.
        </p>
        <p>
          When thoroughly heated, add coconut milk, honey, and
          chopped cilantro. Add salt to taste.
        </p>
    </div>
