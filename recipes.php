    <h2 class="recipe-name">Cuban Black Bean</h2>
    <img class="recipe-img img-left" alt="soup" src="images/soup2.jpg">
    <div class="ingredients">
      <h3>Ingredients:</h3>
        <ul>
          <li>18 cups black beans</li>
          <li>1/3 cups canola oil</li>
          <li>6 large onions, chopped</li>
          <li>10 carrots, chopped</li>
          <li>6 green bell peppers, chopped</li>
          <li>3 chili peppers, minced</li>
          <li>peel and juice of 2 oranges</li>
          <li>8 cloves garlic</li>
          <li>2 large tbsp cumin</li>
          <li>2 large tbsp coriander</li>
          <li>salt</li>
        </ul>
    </div>
    <div class="directions">
      <h3>Directions:</h3>
      <p>
        Sort pebbles and dirt from black beans.
        Put in pot and fill with water. Let sit overnight.
      </p>
      <p>
        Drain beans. Refill pot with water.
        Bring beans to a boil. Reduce heat and keep at a low boil.
      </p>
      <p>
        Sauté onions in canola oil until transparent.
        Add carrots, orange peel and garlic.
        When carrots are tender add peppers, orange juice
        and spices. Stir thoroughly.
      </p>
      <p>
        When peppers are tender and the beans are fully cooked,
        purée all ingredients in blender.
        Add salt and pepper to taste. Reheat and serve.
      </p>
    </div>
  </div>

  <div class="recipe-food">
    <h2 class="recipe-name">Curried Lentil</h2>
    <img class="recipe-img img-right" alt="soup" src="images/soup3.jpg">
    <div class="ingredients">
      <h3>Ingredients:</h3>
        <ul>
          <li>1/3 cup olive oil (or ghee)</li>
          <li>18 cups dried lentils</li>
          <li>1/4 cup grated ginger root</li>
          <li>6 chopped onions</li>
          <li>10 lb sweet potatoes, cubed(1 inch or less)</li>
          <li>6 bell peppers, chopped</li>
          <li>1 tbsp cayenne</li>
          <li>1/3 cup garam masala (blend of aromatic and peppery spices)</li>
          <li>2 tbsp turmeric</li>
          <li>salt</li>
          <li>3 cans coconut milk</li>
          <li>1 bunch chopped fresh cilantro</li>
        </ul>
    </div>
    <div class="directions">
      <h3>Directions:</h3>
      <p>
        Put lentils in 8 qts of water and bring to a boil;
        reduce heat and keep at a slow boil until fully cooked.
        Remove from heat.
      </p>
      <p>
        Sauté the onions and ginger in the oil or ghee.
        When the onions are becoming translucent,
        add the sweet potatoes. Sauté for about 5 minutes,
        stirring to prevent the spices from burning.
      </p>
      <p>
        Add the water. Bring to a boil, and cook until tender.
        Add peppers. Simmer gently another 5 minutes,
        then add coconut milk and cilantro.
        Add salt to taste; adjust the seasonings.
      </p>
    </div>
