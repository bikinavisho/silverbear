    <h2 class="recipe-name">Armenian Lentil</h2>
    <img class="recipe-img img-right" alt="soup" src="images/soup1.jpg">

      <div class="ingredients">
        <h3>Ingredients:</h3>
          <ul>
            <li>18 cups dried lentils </li>
            <li>4 cups apple juice</li>
            <li>1/3 cups olive oil </li>
            <li>6 large onions, chopped</li>
            <li>3 large eggplant, cubed</li>
            <li>8 tomatoes, chopped, or 1 can peeled tomatoes</li>
            <li>8 carrots, chopped</li>
            <li>1 bunch celery, chopped</li>
            <li>3 green peppers, chopped</li>
            <li>1 teaspoon cinnamon</li>
            <li>1 teaspoon ground allspice</li>
            <li>2 tablespoons paprika</li>
            <li>salt</li>
            <li>3 tablespoons chopped fresh parsley</li>
            <li>1 teaspoon dried mint</li>
          </ul>
      </div>
      <div class="directions">
