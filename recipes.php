    <h2 class="recipe-name">Cream of Broccoli</h2>
    <img class="recipe-img img-left" alt="soup" src="images/soup1.jpg">
    <div class="ingredients">
      <h3>Ingredients:</h3>
        <ul>
          <li>1/3 cup of canola oil</li>
          <li>6 onions</li>
          <li>18 potatoes</li>
          <li>8-10 heads (1/2 case) broccoli</li>
          <li>2 tbsp dried dill</li>
          <li>1 tbsp basil</li>
          <li>2-3 qts. whole milk</li>
          <li>salt & pepper</li>
        </ul>
    </div>
    <div class="directions">
      <h3>Directions:</h3>
      <p>
        Peel and chop onions. Sauté in canola oil.
      </p>
      <p>
        Peel and remove bad spots from potatoes. Dice.
        When onions are cooked to translucency, add potatoes.
      </p>
      <p>
        Peel broccoli stems and trim the florets. Put florets aside.
        Dice the most tender parts of the stem.
        Add the diced broccoli stem to the sautéeing potatoes, and onions.
        Add basil and dill.
      </p>
      <p>
        Add water to amply cover the contents of the pot.
        Stirring occasionally to keep from burning, bring to a boil.
      </p>
      <p>
        Bring 2/3 pot of water to a boil.
        Add broccoli florets to water, briefly, to blanch. Drain.
      </p>
      <p>
        Pour a half quart of milk into blender.
        Fill the blender 2/3 full with soup ingredients.
        Cover blender and turn on at low speed.
        Blend for about 20-30 seconds, then shut off.
        Run blender at high speed until soup is smooth.
        Add blanched florets. Salt and pepper to taste.
      </p>
    </div>
  </div>


  <div class="recipe-food">
    <h2 class="recipe-name">Crema de Eloté</h2>
    <img class="recipe-img img-right" alt="soup" src="images/soup3.jpg">
    <div class="ingredients">
      <h3>Ingredients:</h3>
      <ul>
        <li>1/3 cups canola oil</li>
        <li>7.5# frozen corn</li>
        <li>6 large onions, chopped</li>
        <li>8 cloves garlic, minced</li>
        <li>20 large potatoes(8#), cubed</li>
        <li>3-4 chilis(2 oz), chopped</li>
        <li>5 green peppers, chopped</li>
        <li>6 carrots(3#), cubed</li>
        <li>8 fresh tomatoes, cubed</li>
        <li>1 bunch cilantro</li>
        <li>2 Tablespoons coriander</li>
        <li>2 Tablespoons cumin</li>
        <li>2.5 qts milk</li>
        <li>salt to taste</li>
      </ul>
    </div>
    <div class="directions">
      <h3>Directions:</h3>
      <p>
        Sautée chopped onions in canola oil till transparent.
        Add cumin, coriander, and lots of salt.
        Add garlic, chilis, potatoes, and carrots.
        Let vegetables cook 5-10 minutes taking care not to scorch.
      </p>
      <p>
        Add peppers, give them a stir, then add enough water to
        cover the vegetables. Bring water to a boil.
        Stir vegetables often, taking care not to burn.
      </p>
      <p>
        When potatoes and carrots are nearly cooked
        (can be pierced with a fork) add tomatoes,
        half (#3) of the frozen corn and cilantro.
        Continue cooking until potatoes and carrots are tender.
      </p>
      <p>
        Add 1 pint of whole milk to blender,
        add soup ingredients till blender is 2/3 filled.
        Blend on low setting for about 20 seconds.
        Repeat for entire ingredients.
      </p>
      <p>
        Add remaining corn and return to heat.
        Heat thoroughly, stirring often.
      </p>
    </div>
  </div>

