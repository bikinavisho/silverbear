    <h2 class="recipe-name">Tibetan Cauliflower Curry</h2>
    <img class="recipe-img img-left" alt="soup" src="images/soup3.jpg">
    <div class="ingredients">
      <h3>Ingredients:</h3>
        <ul>
          <li>2 Tbsp canola oil</li>
          <li>2 large onions, chopped</li>
          <li>2 garlic cloves, minced</li>
          <li>1 large head cauliflower, chopped</li>
          <li>1 large carrot, cubed</li>
          <li>3 potatoes, cubed</li>
          <li>1 stalk celery, chopped</li>
          <li>1 chili peppers, chopped</li>
          <li>2 tomatoes, chopped</li>
          <li>2 cups, peas</li>
          <li>1 tbsp curry powder</li>
          <li>1 tsp coriander</li>
          <li>1 tsp cumin</li>
          <li>1 tbsp cilantro, chopped</li>
          <li>1 tbsp honey</li>
          <li>1 cup coconut milk</li>
        </ul>
    </div>
    <div class="directions">
      <h3>Directions:</h3>
        <p>
          Sauté onions with garlic in oil until transparent. Add spices and cook
          gently for a few minutes.
        </p>
