            Add eggplant, carrots, and celery. Stir.
            Add cinnamon, paprika, salt and allspice. Add apple juice
            and cook at medium heat until vegetables are nearly tender.
            Add tomatoes, peppers, and dried mint.
          </p>
          <p>
            When all vegetables are fully cooked combine with lentils.
            Add water, if necessary, to bring to the proper consistency.
          </p>
        </div>
