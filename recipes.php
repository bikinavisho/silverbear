      </ul>
    </div>
    <div class="directions">
      <h3>Directions:</h3>
      <p>
        Sauté the leeks in the oil until soft.
        Add salt, tarragon, and potatoes and stir.
        Cover with water and boil until the potatoes are thoroughly cooked.
        Stir occasionally, taking care not to burn.
        Add tomatoes and simmer 5-10 minutes.
      </p>
      <p>
        Purée the cooked vegetables in a blender,
        adding enough whole milk to make the texture and flavor creamy.
        Reheat gently. Stirring often.
      </p>
    </div>
